   private void cargarProductosDisponibles() {
        try (Connection conexion = Conexion.conectar();
             PreparedStatement pst = conexion.prepareStatement(
                 "SELECT id, nombreProducto FROM Productos ORDER BY nombreProducto")) {
            
            ResultSet rs = pst.executeQuery();
            
            jComboBox1.removeAllItems();
            jComboBox1.addItem(new itemProducto(0, "-- No consume producto --"));
            
            while (rs.next()) {
                int id = rs.getInt("id");
                String nombre = rs.getString("nombreProducto");
                jComboBox1.addItem(new itemProducto(id, nombre));
            }
            
        } catch (Exception e) {
            System.out.println("Error cargando productos: " + e.getMessage());
        }
    }
    
    private void seleccionarProducto(int idProducto) {
        for (int i = 0; i < jComboBox1.getItemCount(); i++) {
            Object item = jComboBox1.getItemAt(i);
            if (item instanceof itemProducto) {
                itemProducto prod = (itemProducto) item;
                if (prod.getId() == idProducto) {
                    jComboBox1.setSelectedIndex(i);
                    break;
                }
            }
        }
    }
    
