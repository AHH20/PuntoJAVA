    private boolean validarCampos() {
        // Validar nombre del servicio
        if (txtNombreS1.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Ingrese el nombre del servicio");
            return false;
        }
        
        // Validar precio de venta
        if (txtNombreS2.getText().trim().isEmpty()) {
            JOptionPane.showMessageDialog(this, "Ingrese el precio de venta");
            return false;
        }
        
        try {
            BigDecimal precio = new BigDecimal(txtNombreS2.getText().trim());
            if (precio.compareTo(BigDecimal.ZERO) <= 0) {
                JOptionPane.showMessageDialog(this, "El precio debe ser mayor a 0");
                return false;
            }
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this, "Precio de venta inválido");
            return false;
        }
        
        // Validar si consume producto
        if (Si.isSelected()) {
            // Validar que se seleccionó un producto válido
            itemProducto productoSeleccionado = (itemProducto) jComboBox1.getSelectedItem();
            if (productoSeleccionado == null || productoSeleccionado.getId() == 0) {
                JOptionPane.showMessageDialog(this, "Seleccione un producto válido");
                return false;
            }
            
            if (txtNombreS.getText().trim().isEmpty()) {
                JOptionPane.showMessageDialog(this, "Ingrese la cantidad que consume");
                return false;
            }
            
            try {
                double cantidad = Double.parseDouble(txtNombreS.getText().trim());
                if (cantidad <= 0) {
                    JOptionPane.showMessageDialog(this, "La cantidad debe ser mayor a 0");
                    return false;
                }
            } catch (NumberFormatException e) {
                JOptionPane.showMessageDialog(this, "Cantidad inválida");
                return false;
            }
        }
        
        return true;
    }
    
    
    
    
