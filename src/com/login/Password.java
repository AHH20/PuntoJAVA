    private boolean ValidarCorreo(String Usuario){
    String CorreoRegex = "^[a-zA-Z0-9_+&*-]+(?:\\.[a-zA-Z0-9_+&*-]+)*@(?:[a-zA-Z0-9-]+\\.)+[a-zA-Z]{2,7}$";
            
            return Usuario.matches(CorreoRegex);
    };
    
