    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed

        String Usuario = TxtUsuario.getText();
        String newPass = TxtPassword.getText();
        
        if(Usuario.isEmpty() || newPass.isEmpty()){
        JOptionPane.showMessageDialog(null, "Completa  todo los campos");
        }else if(!ValidarCorreo(Usuario)){
        JOptionPane.showMessageDialog(null, "Debe ingresar un correo valido");
        }else{
        
            try{
            
                String Update = "UPDATE Usuarios SET Contrasena = ? WHERE Correo = ?";
                
                PreparedStatement  ps = db.prepareStatement(Update);
                
                ps.setString(1, newPass);
                ps.setString(2, Usuario);
                ps.executeUpdate();
                
                JOptionPane.showMessageDialog(null, "Contraseña Cambiada correctamente");
            }catch(Exception e){
            
                JOptionPane.showMessageDialog(null, "Correo no encontrado" + e);
            
            }
            
        }
        
        

        
    }//GEN-LAST:event_jButton2ActionPerformed

